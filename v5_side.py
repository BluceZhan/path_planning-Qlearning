        #state = 0
        for j in range(digit):
            next_state_list = []
            for i in range(digit):
                if r[state, i] != -1:
                    next_state_list.append(i)
            next_state = next_state_list[random.randint(0, len(next_state_list) - 1)]
            qval = r[state, next_state] + gamma * max(q[next_state])  # 注意qval是一个数值
